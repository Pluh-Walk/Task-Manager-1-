  public void displayTaskData() {
    try {
        dbConnector dbc = new dbConnector();
        ResultSet rs = dbc.getData("SELECT t_id, title, description, due_date FROM tbl_task"); // Include t_id
        taskTable.setModel(DbUtils.resultSetToTableModel(rs)); 
        rs.close();
        logger.info("Displayed task data successfully.");
    } catch (SQLException ex) {
        logger.log(Level.SEVERE, "Error displaying task data: " + ex.getMessage(), ex);
    } 
}

public void displayUserData() {
    try {
        dbConnector dbc = new dbConnector();
        ResultSet rs = dbc.getData("SELECT u_id, u_name, u_lname, u_email FROM tbl_user");
        usersTable.setModel(DbUtils.resultSetToTableModel(rs)); 
        rs.close();
        logger.info("Displayed user data successfully.");
    } catch (SQLException ex) {
        logger.log(Level.SEVERE, "Error displaying user data: " + ex.getMessage(), ex);
    } 
}

