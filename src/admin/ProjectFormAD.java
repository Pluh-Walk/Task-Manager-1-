    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
   dbConnector dbc = new dbConnector();


    if (pn.getText().isEmpty() || pd.getText().isEmpty() || dd.getText().isEmpty() ||
        ts.getSelectedItem() == null || uid.getText().isEmpty() || tid.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "All fields and selections are required!");
        return;
    }

   
    String projectName = pn.getText();
    String projectDesc = pd.getText();
    String dueDate = dd.getText();
    String status = ts.getSelectedItem().toString();
    String userId = uid.getText(); 
    String taskId = tid.getText(); 

    
    String query = String.format(
        "INSERT INTO tbl_projects (project_name, description, due_date, status, u_id, t_id) " +
        "VALUES ('%s', '%s', '%s', '%s', '%s', '%s')",
        projectName.replace("'", "''"), 
        projectDesc.replace("'", "''"), 
        dueDate.replace("'", "''"), 
        status.replace("'", "''"), 
        userId,
        taskId
    );

   
    if (dbc.insertData(query)) {
        JOptionPane.showMessageDialog(null, "Project added successfully!");
        clearFields();
        displayTaskData();
        displayUserData();
    } else {
        JOptionPane.showMessageDialog(null, "Failed to add project!");
    }
    this.dispose();
    }//GEN-LAST:event_addActionPerformed

