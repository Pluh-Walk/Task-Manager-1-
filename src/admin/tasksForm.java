    private void t_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_updateMouseClicked
        logger.info("Navigating to update task form.");
        int rowIndex = taskTable.getSelectedRow();

if (rowIndex < 0) {
    JOptionPane.showMessageDialog(null, "Please select a task to update!");
    logger.warning("No task selected for update.");
} else {
    try {
        dbConnector dbc = new dbConnector();
        TableModel tbl = tasksTable.getModel();
        ResultSet rs = dbc.getData("SELECT * FROM tbl_tasks WHERE t_id = '" + tbl.getValueAt(rowIndex, 0) + "'");

        if (rs.next()) {
            taskFormAD tf = new taskFormAD(); 
            tf.uid.setText(String.valueOf(rs.getInt("t_id"))); 
            tf.tn.setText(rs.getString("title"));
            tf.td.setText(rs.getString("description"));
            tf.dd.setText(rs.getString("due_date"));
            tf.ts.setSelectedItem(rs.getString("status")); 
           
            
            tf.add.setEnabled(false);
            tf.update.setEnabled(true); 

            tf.setVisible(true);
            this.dispose(); 
            
            dbc.logAction(Session.getInstance().getUid(), "Navigated to update task form for task ID: " + rs.getInt("t_id"));
        }
    } catch (SQLException ex) {
        logger.log(Level.SEVERE, "Error fetching data for task update: " + ex.getMessage(), ex);
    }
}
    }//GEN-LAST:event_t_updateMouseClicked

