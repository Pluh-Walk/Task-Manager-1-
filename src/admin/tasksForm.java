    private void t_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_deleteMouseClicked
      int rowIndex = tasksTable.getSelectedRow();
    logger.info("Navigating to delete a task from the form.");
    if (rowIndex < 0) {
    JOptionPane.showMessageDialog(null, "Please select a task to delete!");
    } else {
    int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this task?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        TableModel tbl = tasksTable.getModel();
        String id = tbl.getValueAt(rowIndex, 0).toString(); // Assuming the first column is the task ID

        dbConnector dbc = new dbConnector();
        try {
            boolean isDeleted = dbc.deleteTaskById(id); // Assuming you have this method in your dbConnector

            if (isDeleted) {
                dbc.logAction(Session.getInstance().getUid(), "Deleted task with ID " + id); // Log deletion
                JOptionPane.showMessageDialog(null, "Task deleted successfully.");
                displayData(); // Refresh the task list
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete task.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
}
    }//GEN-LAST:event_t_deleteMouseClicked

