    private void t_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_deleteMouseClicked
         int rowIndex = projectTable.getSelectedRow();
    logger.info("Attempting to delete a project.");

    if (rowIndex < 0) {
        JOptionPane.showMessageDialog(null, "Please select a project to delete!");
    } else {
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this project?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            TableModel tbl = projectTable.getModel();
            String id = tbl.getValueAt(rowIndex, 0).toString(); // p_id column

            dbConnector dbc = new dbConnector();
            try {
                boolean isDeleted = dbc.deleteProjectById(id); // Call your delete method

                if (isDeleted) {
                    dbc.logAction(Session.getInstance().getUid(), "Deleted project with ID " + id);
                    JOptionPane.showMessageDialog(null, "Project deleted successfully.");
                    displayData(); // Refresh the project data
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to delete project.");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_t_deleteMouseClicked

