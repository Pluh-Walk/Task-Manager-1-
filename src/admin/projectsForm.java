    private void t_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_updateMouseClicked
          logger.info("Navigating to update project form.");
            int rowIndex = projectTable.getSelectedRow();

    if (rowIndex < 0) {
        JOptionPane.showMessageDialog(null, "Please select a project to update!");
        logger.warning("No project selected for update.");
    } else {
        try {
            dbConnector dbc = new dbConnector();
            TableModel tbl = projectTable.getModel();
            String projectId = tbl.getValueAt(rowIndex, 0).toString(); // p_id
            ResultSet rs = dbc.getData("SELECT * FROM tbl_projects WHERE p_id = '" + projectId + "'");

            if (rs.next()) {
                // Create an instance of ProjectFormAD to populate fields
                ProjectFormAD pf = new ProjectFormAD();
                pf.pn.setText(rs.getString("project_name"));
                pf.pd.setText(rs.getString("description")); // If you want this
                pf.dd.setText(rs.getString("due_date"));
                pf.ts.setSelectedItem(rs.getString("status"));
                pf.tid.setText(rs.getString("t_id")); // Task ID
                pf.uid.setText(rs.getString("u_id")); // User ID

                pf.setVisible(true);
                this.dispose(); // Close the current form

                dbc.logAction(Session.getInstance().getUid(), "Navigated to update project form for project ID: " + projectId);
            }
            rs.close();
        } catch (SQLException ex) {
            logger.log(Level.SEVERE, "Error fetching data for project update: " + ex.getMessage(), ex);
        }
    }
    }//GEN-LAST:event_t_updateMouseClicked

